/**
 * 每行前面都有（n-当前行号）个空格。
 */
for ($i = 1; $i <= $n; ++$i) {
    for ($k = 1; $k <= $n - $i; $k++) {
        echo KONGGE;
    }
    for ($m = 1; $m <= 2 * $i - 1; $m++) {
        echo "*";
    }
