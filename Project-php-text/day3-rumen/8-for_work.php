        echo KONGGE;
    }
    /**
     * 每行的第一个和最后一个输出*，其余都输出空格
     */
    for ($m = 1; $m <= 2 * $i - 1; $m++) {

        if ($m == 1 || $m == 2 * $i - 1) {
            echo "*";
        } else {
            echo KONGGE;
        }

    }
