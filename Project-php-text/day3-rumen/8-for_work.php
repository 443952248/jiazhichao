echo "--------------------------------------------------------------------------------------------------------------------------------------------------------------------------<br/>";
echo "星星图B：<br/>";
for ($i = 0; $i < $n; $i++) {
    for ($m = 0; $m <= $i; $m++) {
        echo "*";
    }
    echo "<br/>";
}
echo "<br/>";
echo "--------------------------------------------------------------------------------------------------------------------------------------------------------------------------<br/>";
echo "星星图C：<br/>";
for ($i = 1; $i <= $n; $i++) {
